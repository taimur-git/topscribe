function addTopic(){
    value = topicsinput.value.toLowerCase().replace(/[^a-z0-9 -]/g, '');;
    value.trim();
    if(value!=""){
        addTopicVal(value);
        document.getElementById("topic-input").value ="";
    }
    
}
